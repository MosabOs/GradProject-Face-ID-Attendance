"""
===================================================
تسجيل طالب باستخدام صورة جاهزة (بدون كاميرا)
===================================================
الوصف:
- تختار صورة من جهازك
- تدخل اسم الطالب
- تدخل الرقم الجامعي
- يتم حفظ البيانات في students_data.pkl

طريقة التشغيل:
    python register_student_using_image.py
===================================================
"""

import pickle
import os
import cv2

# ===================================================
# ملف تخزين البيانات
# ===================================================
DATA_FILE = "students_data.pkl"

# ===================================================
# تحميل البيانات (لو موجودة)
# ===================================================
def load_data():
    if not os.path.exists(DATA_FILE):
        # إنشاء هيكل فارغ
        return {
            "images": [],
            "names": [],
            "ids": []
        }

    with open(DATA_FILE, "rb") as f:
        return pickle.load(f)

# ===================================================
# حفظ البيانات
# ===================================================
def save_data(data):
    with open(DATA_FILE, "wb") as f:
        pickle.dump(data, f)

# ===================================================
# التحقق من وجود الطالب
# ===================================================
def student_exists(data, student_id):
    return student_id in data["ids"]

# ===================================================
# إضافة طالب جديد
# ===================================================
def add_student():

    print("=" * 40)
    print("📥 إضافة طالب باستخدام صورة")
    print("=" * 40)

    # إدخال البيانات
    image_path = input("📷 أدخل مسار الصورة: ").strip()
    name = input("👤 اسم الطالب: ").strip()
    student_id = input("🆔 الرقم الجامعي: ").strip()

    # التحقق من الصورة
    if not os.path.exists(image_path):
        print("❌ المسار غير صحيح")
        return

    image = cv2.imread(image_path)

    if image is None:
        print("❌ فشل في قراءة الصورة")
        return

    # تحميل البيانات القديمة
    data = load_data()

    # التحقق من التكرار
    if student_exists(data, student_id):
        print("⚠️ الطالب موجود مسبقاً بنفس الرقم")
        return

    # إضافة البيانات
    data["images"].append(image)
    data["names"].append(name)
    data["ids"].append(student_id)

    # حفظ
    save_data(data)

    print("✅ تم تسجيل الطالب بنجاح!")

# ===================================================
# التشغيل
# ===================================================
if __name__ == "__main__":
    add_student()